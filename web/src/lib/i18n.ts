const LANG_STORAGE_KEY = 'intants:ui-language';

const SUPPORTED = new Set(['en', 'hi', 'te']);

function getStoredLang(): string {
  try {
    const v = localStorage.getItem(LANG_STORAGE_KEY);
    return v && SUPPORTED.has(v) ? v : 'en';
  } catch {
    return 'en';
  }
}

